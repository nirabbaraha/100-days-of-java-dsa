package Day_7;

public class bitManipulationTechnologia {
public static void main(String[] args) {
    //AND
     System.out.print(5&3); 
     //OR
     System.out.println(5|3);
     //XOR
     System.out.println(5^3);
    //  NOT 
    System.out.println(~5);
     //left shift
     System.out.print(5<<1);
     //Right Shift
     System.out.println(5>>1);

}
}